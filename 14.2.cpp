#include <iostream>
using namespace std;

int main() {
	
// "for" ციკლში მეორე ციკლის შემოტანა

	for ( int i = 1; i <= 10; i++ ) { // მოცემული ციკლი ასახავს სტრიქონის რაოდენობის შინაარსს

		for ( int j = 1; j <= 10; j++ ) { // მოცემული ციკლი ასახავს თითო სტრიქონში არსებული ცვლადის შინაარსს
			cout << j << " ";
		}

		cout << "\n";
	}

	return 0;
}