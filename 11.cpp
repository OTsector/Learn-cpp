#include <iostream>
using namespace std;

int main() {
	
	int i = 10;

	i = i +5; // "i" ცვლადის შეესატყვისება: თავის თავს მიმატებული ხუთი

	cout << i << "\n";

	// იგივეა რაც:

	int j = 10;
	
	j += 5; // "j" ცვლადის მნიშვნელბოს გაზრდა ხუთით

	cout << j << "\n";

/*

მინიჭების ოპერატორები თავისი მნიშვნელობით:

"+=" როგორც მეტობა
"-=" როგორც ნაკლებობა
"/=" როგორც გაყოფა
"*=" როგორც გამრავლება

*/
	return 0;
}