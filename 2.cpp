#include <iostream>
using namespace std;

int main() {

	int i; // int ტიპის ცვლადის შემოტანა სახელად: "i"
	i = 8; // "i" მნიშვნელობით უტოლდება 8_ს
	cout << "i = "; // "i" გამოითვალოს
	cout << i; // გამოიტანოს "i"_ს მნიშვნელობა
	cout << "\n"; // "\n" რათა ხაზის გაწყვეტა მოხდეს ფუნქციის დასრულებისას

	cout << "i = " << i << "\n"; // ერთიანი ხაზის გამოტანა "cout"_ით

	return 0;
}